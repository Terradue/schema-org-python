from __future__ import annotations
from datetime import (
    date,
    datetime,
    time
)
from pydantic import (
    field_serializer,
    field_validator,
    AliasChoices,
    BaseModel,
    ConfigDict,
    Field,
    HttpUrl
)
from typing import (
    List,
    Literal,
    Optional,
    Union
)
from .article import Article

class Report(Article):
    '''
    A Report generated by governmental or non-governmental organization.

    Attributes:
        reportNumber: The number or other unique designator assigned to a Report by the publishing organization.
    '''
    class_: Literal['https://schema.org/Report'] = Field( # type: ignore
        default='https://schema.org/Report',
        alias='@type',
        serialization_alias='@type'
    )
    reportNumber: Optional[Union[str, List[str]]] = Field(
        default=None,
        validation_alias=AliasChoices(
            'reportNumber',
            'https://schema.org/reportNumber'
        ),
        serialization_alias='https://schema.org/reportNumber'
    )
